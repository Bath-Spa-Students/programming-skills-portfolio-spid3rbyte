#in original order
places = ["Palestine", "Turkey", "London", "Dhaka", "Oman"]
print(places)
#alphabetical order
print(sorted(places))
print(places)
#reverse alphabetical order 
print(sorted(places, reverse= True))
print(places)
#reversed list 
places.reverse()
print(places)
#reversed original list
places.reverse()
print(places)
#sorted
places.sort()
print(places)
#reverse alphabetical order (again)
places.sort(reverse=True)
print(places)